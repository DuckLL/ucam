普通的網路市場上雖然不乏相機的販賣資訊，但對於要販售的相機都僅有提供其軟硬體資訊、實景照片，無法得知此相機與其他同等相機的優劣勢差在哪，都必須要消費者自己在相關網站找尋找專業評比、使用者的用後心得。
而網路上存在一些資訊網站，提供了比較兩台相機的功能，例如snapsort，但這些網站都只有比較硬體的性能，沒有實際照片的提供，消費者仍然無法直接用肉眼去感受最直接的差距。
這樣的過程對於消費者而言是相當不友善的，因此我們希望提供一個網站，能夠條列比較兩台相機的規格、功能，並對於兩台相機所拍出的各種照片進行喜好度的比較，並且與廠商合作，將消費者導入到合作廠商的購買網站，讓消費者可以一氣呵成的完成挑選與購買的程序，擁有自己的「Ucam」。
